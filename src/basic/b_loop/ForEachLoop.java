package basic.b_loop;

public class ForEachLoop {
    public static void main(String[] args) {
        String[] myFruits = {
                "Apple", "Banana", "Manggo", "Orange", "Grapes"
        };

        // For Each Loop
        for (String i : myFruits) {
            System.out.println(i);
        }
    }
}
