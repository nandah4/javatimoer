package basic.a_datatype;

public class PrimitifType {
    public static void main(String[] args) {
        /*
         * Tipe Data Primif
         * 
         * Tipe data primitif adalah tipe data paling dasar yang sudah ada di dalam
         * Java, dan tidak bisa
         * dipecah lagi. Tipe ini digunakan untuk menyimpan nilai-nilai sederhana,
         * seperti angka,
         * karakter, dan boolean (true/false). Setiap tipe data primitif menyimpan nilai
         * langsung dalam
         * variabel.
         * 
         * 
         * byte, short, int, long, float, double, char, boolean
         * 
         */

        byte byteNumber = 127;
        System.out.println(byteNumber);

        short shortNumber = 32767;
        System.out.println(shortNumber);

        float floatNumber = 20.2f;
        System.out.println(floatNumber);

        double doubleNumber;
        doubleNumber = 92.19;
        System.out.println(doubleNumber);
    }
}
